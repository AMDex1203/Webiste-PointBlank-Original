				return;
			}
		    
		    var checkid = $("#userid").attr("checkid");
		    
		    if (typeof checkid == "undefined" || ($.trim(checkid) != "" && checkid != "1")) {
				drawMsg("userid", "Silahkan konfirmasi ID.");
		        return;
			}
		    
