		    var regNum = /[0-9]/g;

			if ($.trim($("#password").val()).length <= 0) {
				drawMsg("password", "Silakan masukan kata sandi anda.");
				return;
			} else  if ($.trim($("#password").val()).length < 8 || $.trim($("#password").val()).length > 16) {
				drawMsg("password", "Masukan kata sandi anda dari 8 sampai 16 karakter.");
				return;
